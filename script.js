//функция получения дня недели и даты выбранного дня
  function getActiveDay(date) {
    const day = new Date(year, month, date);
    const dayName = day.toString().split(" ")[0];
    eventDay.innerHTML = dayName;
    eventDate.innerHTML = date + " " + months[month] + " " + year;
    //перевод названий дня недели на русский
    if (dayName === "Mon"){eventDay.innerHTML = "Понедельник";}
    else if (dayName === "Tue") {eventDay.innerHTML = "Вторник";}
    else if (dayName === "Wed") {eventDay.innerHTML = "Среда";}
    else if (dayName === "Thu") {eventDay.innerHTML = "Четверг";}
    else if (dayName === "Fri") {eventDay.innerHTML = "Пятница";}
    else if (dayName === "Sat") {eventDay.innerHTML = "Суббота";}
    else eventDay.innerHTML = "Воскресенье";
  }


function updateEvents(date) {
  let events = "";
  eventsArr.forEach((event) => {
    if (
      date === event.day &&
      month + 1 === event.month &&
      year === event.year
    ) {
      event.events.forEach((event) => {
        events += `<div class="event">
            <div class="title">
              <i class="fas fa-circle"></i>
              <h3 class="event-title">${event.title}</h3>
            </div>
            <div class="event-time">
              <span class="event-time">${event.time}</span>
            </div>
        </div>`;
      });
    }
  });
  if (events === "") {
    events = `<div class="no-event">
            <h3>Пусто</h3>
        </div>`;
  }
  eventsContainer.innerHTML = events;
  saveEvents();
}

